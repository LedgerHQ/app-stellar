/// Looks up token information by contract address
///
/// Uses linear search (O(n)), but with a few tokens, this is very fast
///
/// # Arguments
/// * `contract_address` - The 32-byte contract address
///
/// # Returns
/// Token information if the contract is in the registry, None otherwise
pub fn get_token_info(contract_address: &[u8; 32]) -> Option<&'static TokenInfo> {
    KNOWN_TOKENS
        .iter()
        .find(|token| &token.contract_address == contract_address)
}
